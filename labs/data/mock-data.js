window.LabsHelpers = {
  formatCurrency: function(amount, currency) {
    currency = currency || 'EUR';
    var sym = currency === 'GBP' ? '£' : '€';
    return sym + amount.toLocaleString();
  },

  formatDate: function(dateStr) {
    if (!dateStr) return '--';
    var d = new Date(dateStr);
    return d.toLocaleDateString('en-IE', { day: 'numeric', month: 'short', year: 'numeric' });
  },

  formatDateShort: function(dateStr) {
    if (!dateStr) return '--';
    var d = new Date(dateStr);
    return d.toLocaleDateString('en-IE', { day: 'numeric', month: 'short' });
  },

  daysUntil: function(dateStr) {
    if (!dateStr) return null;
    var diff = (new Date(dateStr) - new Date()) / (1000 * 60 * 60 * 24);
    return Math.ceil(diff);
  },

  getScoreColor: function(score) {
    if (score >= 80) return 'var(--accent-green)';
    if (score >= 60) return 'var(--accent-blue)';
    if (score >= 40) return 'var(--accent-amber)';
    return 'var(--accent-red)';
  },

  getRiskColor: function(score) {
    if (score <= 20) return 'var(--accent-green)';
    if (score <= 40) return 'var(--accent-amber)';
    return 'var(--accent-red)';
  },

  getStatusBadge: function(status) {
    var map = {
      'available': 'green', 'limited': 'amber', 'busy': 'red',
      'active': 'blue', 'planning': 'amber', 'completed': 'green', 'paused': 'red',
      'confirmed': 'green', 'advance': 'blue', 'pending': 'amber',
      'draft': 'amber', 'in_progress': 'blue', 'approved': 'green',
      'signed': 'green', 'sent': 'blue', 'not_sent': 'red'
    };
    return map[status] || map[status.toLowerCase()] || 'purple';
  },

  simulateScenarios: function(opportunity, goal) {
    var scenarios = window.LabsData.scenarios.filter(function(s) {
      return s.opportunityId === opportunity.id;
    });

    if (scenarios.length === 0) return [];

    var weights = {
      'Maximize ROI': { roi: 0.4, profit: 0.3, risk: 0.2, growth: 0.1 },
      'Maximize Artist Growth': { growth: 0.4, brand: 0.2, risk: 0.2, roi: 0.2 },
      'Maximize Brand Visibility': { brand: 0.4, marketing: 0.3, growth: 0.2, risk: 0.1 },
      'Reduce Risk': { risk: 0.5, roi: 0.2, profit: 0.2, growth: 0.1 },
      'Maximize Client Satisfaction': { client: 0.4, risk: 0.2, roi: 0.2, brand: 0.2 },
      'Balanced': { roi: 0.2, growth: 0.2, brand: 0.2, risk: 0.2, marketing: 0.2 }
    };

    var w = weights[goal] || weights['Balanced'];

    scenarios.forEach(function(s) {
      s.compositeScore =
        (w.roi || 0) * (s.roi / 100) +
        (w.profit || 0) * (s.profitMargin / 100) +
        (w.growth || 0) * (s.artistGrowthScore / 100) +
        (w.brand || 0) * (s.brandScore / 100) +
        (w.risk || 0) * ((100 - s.riskScore) / 100) +
        (w.marketing || 0) * (s.marketingImpact / 100) +
        (w.client || 0) * (s.clientRelationship / 100);
      s.compositeScore = Math.round(s.compositeScore * 100);
    });

    scenarios.sort(function(a, b) { return b.compositeScore - a.compositeScore; });
    scenarios[0].recommended = true;
    for (var i = 1; i < scenarios.length; i++) scenarios[i].recommended = false;

    return scenarios;
  }
};
